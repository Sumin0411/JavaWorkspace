package chap2;

public class _01_Operator {
    public static void main(String[] args) {
        int num = 10;
        num = num +2;
        System.out.println(num);

        num = num -2;
        System.out.println(num);

        //복합연산자
        num = 10;
//        num = num + 2;
        num += 2;
        System.out.println(num);

//        num = num -2;
        System.out.println(num);

//        num = num *2;
        System.out.println(num);
    }
}